package functions;

public class Calculator {

	public int add(int a, int b) {
		int c = a + b;
		return c;
	}

	public int sub(int a, int b) {
		int c = a - b;
		return c;
	}

	public int mul(int a, int b) {
		int c = a * b;
		return c;
	}

	public int div(int a, int b) {
		int c = a - b;
		return c;
	}

	public void addv(int a, int b) {
		int c = a + b;
		System.out.println(c);
	}

	public void subv(int a, int b) {
		int c = a - b;
		System.out.println(c);
	}

	public void mulv(int a, int b) {
		int c = a * b;
		System.out.println(c);
	}

	public void divv(int a, int b) {
		int c = a - b;
		System.out.println(c);
	}

}
